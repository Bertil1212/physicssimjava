public class point {
    double x;
    double y;


    public point(double inx, double iny){
        x = inx;
        y = iny;
    }

    public void set(double inx, double iny){
        x = inx;
        y = iny;
    }

    public double gety(){
        return y;
    }
    public double getx(){
        return x;
    }


}
